/**
 * 多语言字段访问辅助函数
 * 根据当前语言选取规则数据中的对应字段
 * 
 * @param {object} rule - 规则数据对象
 * @param {string} fieldBase - 基础字段名（如 'name', 'summary', 'riskPoint'）
 * @param {string} lang - 语言代码
 * @returns {string} 对应语言的文本，无翻译时返回 undefined
 */
function ruleField(rule, fieldBase, lang) {
  if (lang === "en") return rule[fieldBase];
  if (lang === "zh") return rule[fieldBase + "CN"] || rule[fieldBase];
  // 其他语言：查找对应字段，不存在则降级到英语
  const langKey = fieldBase + "_" + lang;
  return rule[langKey] || rule[fieldBase];
}

/**
 * 便捷翻译函数（供 app 对象内部使用）
 * this.lang 为当前语言，返回对应翻译
 */
function T(lang, key, ...args) {
  let text = (i18n[lang] && i18n[lang][key]) || i18n["en"][key] || key;
  // 支持简单占位替换 {0}, {1}
  args.forEach((arg, i) => {
    text = text.replace("{" + i + "}", arg);
  });
  return text;
}
